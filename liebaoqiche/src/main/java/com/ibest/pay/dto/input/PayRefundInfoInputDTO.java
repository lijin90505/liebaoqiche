package com.ibest.pay.dto.input;

import com.ibest.framework.common.persistence.BaseInputDTO;

public class PayRefundInfoInputDTO extends BaseInputDTO {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String refundNo;
	
	private String outTradeNo;
	
	private String outRefundNo;
	
	private String refundStartTime;
	
	private String refundStatus;

	public String getRefundNo() {
		return refundNo;
	}

	public void setRefundNo(String refundNo) {
		this.refundNo = refundNo;
	}

	public String getOutTradeNo() {
		return outTradeNo;
	}

	public void setOutTradeNo(String outTradeNo) {
		this.outTradeNo = outTradeNo;
	}

	public String getOutRefundNo() {
		return outRefundNo;
	}

	public void setOutRefundNo(String outRefundNo) {
		this.outRefundNo = outRefundNo;
	}

	public String getRefundStartTime() {
		return refundStartTime;
	}

	public void setRefundStartTime(String refundStartTime) {
		this.refundStartTime = refundStartTime;
	}

	public String getRefundStatus() {
		return refundStatus;
	}

	public void setRefundStatus(String refundStatus) {
		this.refundStatus = refundStatus;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("PayRefundInfoInputDTO [refundNo=");
		builder.append(refundNo);
		builder.append(", outTradeNo=");
		builder.append(outTradeNo);
		builder.append(", outRefundNo=");
		builder.append(outRefundNo);
		builder.append(", refundStartTime=");
		builder.append(refundStartTime);
		builder.append(", refundStatus=");
		builder.append(refundStatus);
		builder.append("]");
		return builder.toString();
	}
}
