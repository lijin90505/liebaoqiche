package com.ibest.pay.entity;

import java.util.Date;

import com.ibest.framework.common.persistence.BaseEntity;

public class OutSystemOrder extends BaseEntity {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

					
	/**
	 * 订单号
	 */ 
	private String orderId;
				
	/**
	 * 支付类型
	 */ 
	private String payType;
				
	/**
	 * 接入系统标识
	 */ 
	private String outSystemId;
				
	/**
	 * 接入设备
	 */ 
	private String deviceId;
				
	/**
	 * 订单状态
	 */ 
	private String orderStatus;
				
	/**
	 * 订单金额
	 */ 
	private String amount;
				
	/**
	 * 订单支付时间
	 */ 
	private String orderPayTime;
				
	/**
	 * 用户标识
	 */ 
	private String userid;
																	
	public String getOrderId(){
		return orderId;
	}
	
	public void setOrderId(String orderId){
		this.orderId = orderId;
	}
				
	public String getPayType(){
		return payType;
	}
	
	public void setPayType(String payType){
		this.payType = payType;
	}
				
	public String getOutSystemId(){
		return outSystemId;
	}
	
	public void setOutSystemId(String outSystemId){
		this.outSystemId = outSystemId;
	}
				
	public String getDeviceId(){
		return deviceId;
	}
	
	public void setDeviceId(String deviceId){
		this.deviceId = deviceId;
	}
				
	public String getOrderStatus(){
		return orderStatus;
	}
	
	public void setOrderStatus(String orderStatus){
		this.orderStatus = orderStatus;
	}
				
	public String getAmount(){
		return amount;
	}
	
	public void setAmount(String amount){
		this.amount = amount;
	}
				
	public String getOrderPayTime(){
		return orderPayTime;
	}
	
	public void setOrderPayTime(String orderPayTime){
		this.orderPayTime = orderPayTime;
	}
				
	public String getUserid(){
		return userid;
	}
	
	public void setUserid(String userid){
		this.userid = userid;
	}
													
}
