package com.ibest.integral.entity;

import java.util.Date;

import com.ibest.framework.common.persistence.BaseEntity;

public class Integral extends BaseEntity {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

					
	// 积分名称
	private String name;
				
	// 积分数
	private Integer integral;
				
	// 积分类别编号
	private String integralCategoryId;

	private String integralCategoryName;
						
	private String state;
	
	// 创建人
	private String creater;
				
	// 创建时间
	private Date createTime;
				
	// 修改人
	private String modifier;
				
	// 修改时间
	private Date modifyTime;
				
	// 版本
	private String version;
								
	
	
	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getName(){
		return name;
	}
	
	public void setName(String name){
		this.name = name;
	}
				
	public Integer getIntegral(){
		return integral;
	}
	
	public void setIntegral(Integer integral){
		this.integral = integral;
	}
				
	public String getIntegralCategoryId(){
		return integralCategoryId;
	}
	
	public void setIntegralCategoryId(String integralCategoryId){
		this.integralCategoryId = integralCategoryId;
	}

	public String getIntegralCategoryName() {
		return integralCategoryName;
	}

	public void setIntegralCategoryName(String integralCategoryName) {
		this.integralCategoryName = integralCategoryName;
	}

	public String getCreater(){
		return creater;
	}
	
	public void setCreater(String creater){
		this.creater = creater;
	}
				
	public Date getCreateTime(){
		return createTime;
	}
	
	public void setCreateTime(Date createTime){
		this.createTime = createTime;
	}
				
	public String getModifier(){
		return modifier;
	}
	
	public void setModifier(String modifier){
		this.modifier = modifier;
	}
				
	public Date getModifyTime(){
		return modifyTime;
	}
	
	public void setModifyTime(Date modifyTime){
		this.modifyTime = modifyTime;
	}
				
	public String getVersion(){
		return version;
	}
	
	public void setVersion(String version){
		this.version = version;
	}
					
}
